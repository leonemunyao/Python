# RSA (Cryptosystem)

# RSA stands for Rivest-Shamir-Adleman. Its a public key cryptosystem one of the oldest that is used for secure data transmission.
# RSA is a widely used public-key cryptography algorithm created by Ron Rivest, Adi Shamir, and Leonard Adleman. They publicly described
# the algorithm in 1977. In a public key cryptosystem, the encryption key is public and distinct from the decryption key which
# is kept secret(private). An RSA user user creates and publishes a public key based on two large prime numbers(the factoring problem).
# Breaking RSA encryption is known as RSA problem. RSA is used to transmit shared keys for symetric-key cryptography which are then used
# for bulk encryption-decryption. 